export class Viking {
    public nom : string;
    public sante : string;
    public force : string;
    public arme : string;

    constructor(
        nom: string,
        sante: string,
        force: string,
        arme: string,
    ) {
        this.nom = nom
        this.sante = sante
        this.force = force
        this.arme = arme
    }
}